
"use client";

import { useSearchParams, useRouter } from "next/navigation";
import { useRef } from "react";
import ResultChart from "../../components/ResultChart";
import StepIndicator from "../../components/StepIndicator";
import jsPDF from "jspdf";
import CountUp from "react-countup";

export default function ResultPage(){

const params = useSearchParams();
const router = useRouter();
const reportRef = useRef<HTMLDivElement>(null);

const goal = params.get("goal");
const cost = Number(params.get("cost"));
const years = Number(params.get("years"));
const inflation = Number(params.get("inflation"));
const future = Number(params.get("future"));
const sip = Number(params.get("sip"));
const returnRate = Number(params.get("returnRate"));

/* Currency Formatter */

const formatCurrency = (num:number)=>{
return new Intl.NumberFormat("en-IN",{
style:"currency",
currency:"INR",
maximumFractionDigits:0
}).format(num);
};

/* Financial Metrics */

const inflationImpact = future - cost;
const totalInvestment = sip * years * 12;
const wealthGained = future - totalInvestment;

/* Goal Timer */

const goalDate = new Date();
goalDate.setFullYear(goalDate.getFullYear() + years);

const today = new Date();
const remainingYears = goalDate.getFullYear() - today.getFullYear();
const remainingMonths = 12 - today.getMonth();

/* AI-like Insight (No API) */

function generateInsight(){

let message = "";

if(sip < 500){
message += "Your required SIP is relatively small, indicating the goal is manageable within the given timeline. ";
}
else if(sip < 2000){
message += "Your SIP requirement is moderate and achievable with disciplined monthly investing. ";
}
else{
message += "Your SIP requirement is relatively high, which may require increasing investment duration or contributions. ";
}

if(returnRate >= 15){
message += "The expected return assumption is optimistic and may involve higher-risk investments. ";
}
else if(returnRate >= 10){
message += "The expected return aligns with typical long-term equity market averages. ";
}
else{
message += "The expected return assumption is conservative and may produce slower but stable growth. ";
}

if(inflation >= 8){
message += "Higher inflation significantly increases the future goal cost, making early investing very important.";
}
else{
message += "Moderate inflation assumptions make the financial goal easier to plan.";
}

return message;

}

const aiAdvice = generateInsight();

/* PDF Download */

const downloadPDF = () => {

const pdf = new jsPDF();

pdf.setFontSize(20);
pdf.text("Investment Report",20,20);

pdf.setFontSize(12);

pdf.text(`Goal: ${goal}`,20,40);
pdf.text(`Current Cost: ${formatCurrency(cost)}`,20,50);
pdf.text(`Years: ${years}`,20,60);
pdf.text(`Inflation: ${inflation}%`,20,70);
pdf.text(`Expected Return: ${returnRate}%`,20,80);

pdf.text(`Future Goal Value: ${formatCurrency(future)}`,20,100);
pdf.text(`Required Monthly SIP: ${formatCurrency(sip)}`,20,110);

pdf.text(`Total Investment: ${formatCurrency(totalInvestment)}`,20,130);
pdf.text(`Wealth Gained: ${formatCurrency(wealthGained)}`,20,140);

pdf.text(`Inflation Impact: ${formatCurrency(inflationImpact)}`,20,150);
pdf.text("Generated by FinCal Goal Planner",20,180);

pdf.save("Investment_Report.pdf");

};

return(

<>
<StepIndicator step={3}/>

<main className="min-h-screen flex items-center justify-center bg-gradient-to-br from-[#0f172a] to-[#1e293b] px-6 py-12">

<div
ref={reportRef}
className="bg-white/10 backdrop-blur-lg border border-white/20 
p-6 sm:p-8 md:p-10 
rounded-xl 
w-full max-w-4xl 
text-white"
>

<h1 className="text-3xl font-bold text-center bg-gradient-to-r from-cyan-400 to-blue-500 bg-clip-text text-transparent">
Investment Result
</h1>

{/* RESULT CARDS */}

<div className="grid grid-cols-1 sm:grid-cols-2 gap-4 mt-6">

<div className="bg-black/30 border border-white/10 rounded-lg p-4">
<p className="text-xs text-gray-400">Future Goal Value</p>
<p className="text-xl font-semibold text-cyan-300">
₹ <CountUp end={future} duration={2} separator=","/>
</p>
</div>

<div className="bg-black/30 border border-white/10 rounded-lg p-4">
<p className="text-xs text-gray-400">Required Monthly SIP</p>
<p className="text-xl font-semibold text-green-400">
₹ <CountUp end={sip} duration={2} separator=","/>
</p>
</div>

</div>

<p className="mt-4 text-gray-300">
Due to {inflation}% inflation, your {goal} costing {formatCurrency(cost)}
today may cost approximately {formatCurrency(future)} after {years} years.
</p>

{/* CHART */}

<div className="bg-cyan-500/20 border border-cyan-400 text-cyan-300 px-3 py-1 rounded text-sm w-fit mb-4 mt-6">
Goal Planning Projection
</div>

<ResultChart
future={future}
years={years}
sip={sip}
returnRate={returnRate}
/>

{/* GOAL PROGRESS */}

<div className="mt-6">

<p className="text-sm text-gray-300 mb-2">
Goal Achievement Projection
</p>

<div className="w-full bg-gray-700 rounded-full h-4">

<div
className="bg-cyan-400 h-4 rounded-full transition-all duration-700"
style={{width:"100%"}}
></div>

</div>

<p className="text-xs text-gray-400 mt-2">
Your investment plan is projected to achieve the target goal value.
</p>

</div>

{/* GOAL SUCCESS */}

<div className="mt-6 bg-white/10 border border-white/20 p-4 rounded-lg">

<p className="text-sm text-gray-300 mb-2">Goal Success Indicator</p>

<div className="flex items-center gap-2">
<div className="w-3 h-3 bg-green-400 rounded-full"></div>
<p className="text-green-300 text-sm font-semibold">
High Probability of Achieving Goal
</p>
</div>

</div>

{/* TIMER */}

<div className="mt-6 bg-blue-500/10 border border-blue-400 p-4 rounded-lg">

<p className="font-semibold text-blue-300">
Goal Achievement Timer
</p>

<p className="text-sm text-gray-300 mt-1">
Approximately {remainingYears} years and {remainingMonths} months remaining to achieve your goal.
</p>

</div>

{/* BREAKDOWN */}

<div className="grid grid-cols-1 sm:grid-cols-3 gap-4 mt-6">
    
<div className="bg-black/30 p-4 rounded-lg border border-white/10">
<p className="text-xs text-gray-400">Total Invested</p>
<p className="text-cyan-300 font-semibold">{formatCurrency(totalInvestment)}</p>
</div>

<div className="bg-black/30 p-4 rounded-lg border border-white/10">
<p className="text-xs text-gray-400">Wealth Gained</p>
<p className="text-green-400 font-semibold">{formatCurrency(wealthGained)}</p>
</div>

<div className="bg-black/30 p-4 rounded-lg border border-white/10">
<p className="text-xs text-gray-400">Final Value</p>
<p className="text-white font-semibold">{formatCurrency(future)}</p>
</div>

</div>

{/* AI INSIGHT */}

<div className="bg-blue-500/10 border border-blue-400 p-4 rounded-lg mt-6">

<p className="font-semibold text-blue-300">
AI Financial Insight
</p>

<p className="text-sm text-gray-300 mt-2">
{aiAdvice}
</p>

</div>

{/* DISCLAIMER */}

<div className="mt-8 bg-gray-800/40 border border-gray-600 p-4 rounded-lg text-xs text-gray-300">

<p className="font-semibold mb-2">Compliance Disclaimer</p>

<p>
This tool is designed for informational purposes only. Actual results
may vary depending on market conditions and investment performance.
This should not be considered financial advice.
</p>

</div>

{/* BUTTONS */}

<div className="flex justify-center mt-6">

<button
onClick={downloadPDF}
className="bg-gradient-to-r from-blue-500 to-cyan-400 px-6 py-2 rounded-lg font-semibold hover:scale-105 transition"
>
Download Investment Report (PDF)
</button>

</div>

<div className="flex justify-center mt-6">

<button
onClick={()=>router.push("/")}
className="bg-white/10 border border-white/20 px-6 py-2 rounded-lg hover:bg-white/20 transition"
>
← Back to Calculator
</button>

</div>

</div>

</main>
</>

);

}